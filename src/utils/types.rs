//! 
//! types.rs
//! 
//! Generic type declarations for the kernel.
//! 

#[allow(non_camel_case_types)]
/// Represents 8 bits
pub type BYTE = u8;
#[allow(non_camel_case_types)]
/// Represents 16 bits
pub type WORD = u16;
#[allow(non_camel_case_types)]
/// Represents 32 bits
pub type DWORD = u32;
#[allow(non_camel_case_types)]
/// Represents 64 bits
pub type QWORD = u64;